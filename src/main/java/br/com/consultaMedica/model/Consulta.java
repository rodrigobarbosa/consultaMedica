package br.com.consultaMedica.model;

public class Consulta {

	private String numeroConsulta;
	private String atendenteConsulta;
	
	public String getNumeroConsulta() {
		return numeroConsulta;
	}
	public void setNumeroConsulta(String numeroConsulta) {
		this.numeroConsulta = numeroConsulta;
	}
	public String getAtendenteConsulta() {
		return atendenteConsulta;
	}
	public void setAtendenteConsulta(String atendenteConsulta) {
		this.atendenteConsulta = atendenteConsulta;
	}
	
}
